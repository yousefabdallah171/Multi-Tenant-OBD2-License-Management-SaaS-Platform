export function normalizeCountryName(value: string | null | undefined): string {
  const trimmed = (value ?? '').trim()
  if (!trimmed) return ''
  return COUNTRY_LOOKUP.get(trimmed.toLowerCase()) ?? trimmed
}

export function getCountryCodeByName(value: string | null | undefined): string {
  const normalized = normalizeCountryName(value).toLowerCase()
  if (!normalized) return ''

  const alias = COUNTRY_ALIASES[normalized] ?? normalized
  return COUNTRY_CODE_LOOKUP.get(alias) ?? ''
}
