const regionNames = typeof Intl !== 'undefined'
  ? new Intl.DisplayNames(['en'], { type: 'region' })
  : null

const COUNTRY_CODE_LOOKUP = new Map<string, string>()

for (const code of ISO_REGION_CODES) {
  const name = regionNames?.of(code)
  if (!name) continue
  COUNTRY_CODE_LOOKUP.set(name.toLowerCase(), code.toLowerCase())
}

